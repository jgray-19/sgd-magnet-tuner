def plot_fullring_comparison(
    accelerator: LHC,
    all_estimates: dict[str, float],
    analysis_dir: Path,
    squeeze_step: str,
    results_dir: Path,
    tune_knobs_file: Path,
    corrector_file: Path | None,
    beam: int,
    deltap: float = 0.0,
) -> None:
    """Plot full-ring optics differences relative to the measurement."""
    meas_twiss, _ = build_twiss_from_measurements(
        analysis_dir, include_errors=True, reverse_bpm_order=beam == 2
    )
    meas_twiss.columns = [col.lower() for col in meas_twiss.columns]

    twiss_basic = get_fullring_twiss(accelerator, deltap=deltap)
    twiss_online = get_fullring_twiss(
        accelerator,
        tune_knobs_file=tune_knobs_file,
        corrector_file=corrector_file,
        deltap=deltap,
    )
    twiss_eff_online = get_fullring_twiss(
        accelerator,
        estimated_magnets=all_estimates,
        tune_knobs_file=tune_knobs_file,
        corrector_file=corrector_file,
        deltap=deltap,
    )
    ip_positions = get_ip_positions(accelerator)

    common_bpms = (
        meas_twiss.index.intersection(twiss_basic.index)
        .intersection(twiss_online.index)
        .intersection(twiss_eff_online.index)
    )
    if len(common_bpms) == 0:
        raise ValueError("No common BPMs found for full-ring comparison.")

    meas_full = meas_twiss.loc[common_bpms].copy()
    base_full = twiss_basic.loc[common_bpms].copy()
    online_full = twiss_online.loc[common_bpms].copy()
    est_full = twiss_eff_online.loc[common_bpms].copy()

    start_bpm = common_bpms[0]
    meas_full = _normalize_phase(meas_full, ("mux", "muy"), start_bpm)
    base_full = _normalize_phase(base_full, ("mux", "muy"), start_bpm)
    online_full = _normalize_phase(online_full, ("mux", "muy"), start_bpm)
    est_full = _normalize_phase(est_full, ("mux", "muy"), start_bpm)

    fig, axes = plt.subplots(2, 2, figsize=(16, 10), sharex=True)
    xvals = meas_full["s"]

    def _diff(model: pd.Series, measurement: pd.Series) -> pd.Series:
        return model - measurement

    def _rel_diff(model: pd.Series, measurement: pd.Series) -> pd.Series:
        return (model - measurement) / measurement * 100.0

    series_to_plot = [
        ("Base model - meas", base_full, "s-", 1.0),
        ("Model+tunes - meas", online_full, "^-", 1.0),
        ("Model+tunes+ests - meas", est_full, "d-", 1.0),
    ]
    plot_specs = [
        (axes[0, 0], "mux", "phase", "Delta mu1 (2pi)"),
        (axes[0, 1], "muy", "phase", "Delta mu2 (2pi)"),
        (axes[1, 0], "betx", "beta", "Delta beta_x / beta_x vs meas (%)"),
        (axes[1, 1], "bety", "beta", "Delta beta_y / beta_y vs meas (%)"),
    ]

    for ax, column, plot_type, ylabel in plot_specs:
        for label, df, fmt, linewidth in series_to_plot:
            if plot_type == "phase":
                yvals = _diff(df[column], meas_full[column])
            else:
                yvals = _rel_diff(df[column], meas_full[column])
            ax.plot(
                xvals,
                yvals,
                fmt,
                markersize=2.5,
                linewidth=linewidth,
                label=label,
                alpha=0.9,
            )
        ax.axhline(0.0, color="k", linewidth=0.8, alpha=0.5)
        ax.set_ylabel(ylabel)
        ax.grid(visible=True, alpha=0.3)
        ax.legend(fontsize=8)
        add_ip_positions_to_plot(ax, ip_positions)

    axes[0, 0].set_title("Full Ring - Horizontal Phase Delta")
    axes[0, 1].set_title("Full Ring - Vertical Phase Delta")
    axes[1, 0].set_title("Full Ring - Horizontal Beta Delta")
    axes[1, 1].set_title("Full Ring - Vertical Beta Delta")
    axes[1, 0].set_xlabel("S (m)")
    axes[1, 1].set_xlabel("S (m)")

    fig.suptitle(
        f"Full-ring optics deltas vs measurement - {squeeze_step} (phase origin: {start_bpm})",
        fontsize=12,
    )
    plt.tight_layout()
    plt.savefig(results_dir / f"phase_advance_{squeeze_step}_fullring.png", dpi=150)

    # Closed-orbit comparison on a separate figure.
    def _pick_orbit_col(df: pd.DataFrame, candidates: tuple[str, ...]) -> str | None:
        for col in candidates:
            if col in df.columns:
                return col
        return None

    x_col = _pick_orbit_col(meas_full, ("x", "orbitx", "cox"))
    y_col = _pick_orbit_col(meas_full, ("y", "orbity", "coy"))

    if x_col is None and y_col is None:
        LOGGER.warning(
            "No closed-orbit columns found in measurement twiss. Skipping full-ring closed-orbit plot."
        )
    else:
        fig_orbit, orbit_axes = plt.subplots(2, 1, figsize=(16, 8), sharex=True)
        orbit_specs = [
            (orbit_axes[0], x_col, "Horizontal closed orbit", "x (m)"),
            (orbit_axes[1], y_col, "Vertical closed orbit", "y (m)"),
        ]

        for ax, col, title, ylabel in orbit_specs:
            if col is None:
                ax.text(
                    0.5,
                    0.5,
                    "Orbit column unavailable in measurement",
                    transform=ax.transAxes,
                    ha="center",
                    va="center",
                )
                ax.set_title(title)
                ax.grid(visible=True, alpha=0.3)
                add_ip_positions_to_plot(ax, ip_positions)
                continue

            ax.plot(
                xvals,
                meas_full[col],
                "o-",
                markersize=2.5,
                linewidth=1.0,
                label="Measurement",
                alpha=0.9,
            )
            ax.plot(
                xvals,
                base_full[col],
                "s-",
                markersize=2.5,
                linewidth=1.0,
                label="Base model",
                alpha=0.9,
            )
            ax.plot(
                xvals,
                online_full[col],
                "^-",
                markersize=2.5,
                linewidth=1.0,
                label="Model+tunes",
                alpha=0.9,
            )
            ax.plot(
                xvals,
                est_full[col],
                "d-",
                markersize=2.5,
                linewidth=1.0,
                label="Model+tunes+ests",
                alpha=0.9,
            )
            ax.set_title(title)
            ax.set_ylabel(ylabel)
            ax.grid(visible=True, alpha=0.3)
            ax.legend(fontsize=8)
            add_ip_positions_to_plot(ax, ip_positions)

        orbit_axes[1].set_xlabel("S (m)")
        fig_orbit.suptitle(
            f"Full-ring closed orbit values - {squeeze_step}",
            fontsize=12,
        )
        plt.tight_layout()
        plt.savefig(results_dir / f"closed_orbit_{squeeze_step}_fullring.png", dpi=150)

    plt.show()


def main():
    """Run the plotting command-line entry point."""
    logging.basicConfig(level=logging.INFO)

    parser = argparse.ArgumentParser()
    parser.add_argument("--beam", type=int, choices=[1, 2], required=True, help="Beam number")
    parser.add_argument(
        "--squeeze-step", type=str, required=True, help="Squeeze step, e.g., '1.2m'"
    )
    parser.add_argument(
        "--optics", action="store_true", help="Use the optimisation from optics measurements"
    )
    parser.add_argument(
        "--arcs",
        type=str,
        default=None,
        help="Arcs to plot: can be a range (e.g., '4-6') or comma-separated (e.g., '5,6,7'). If not specified, plots all arcs 1-8.",
    )
    parser.add_argument(
        "--frequency",
        type=str,
        default="0Hz",
        help="Frequency key used to load tune/corrector files (e.g., '0Hz', '+50Hz', '-50Hz').",
    )
    parser.add_argument(
        "--fullring",
        action="store_true",
        help="Plot a single full-ring comparison of mu1, mu2, betax, and betay for the three model cases.",
    )
    args = parser.parse_args()
    beam = args.beam
    squeeze_step = args.squeeze_step
    use_optics = args.optics
    frequency = args.frequency

    # Parse arcs argument
    arc_list = None
    if args.arcs:
        if "-" in args.arcs:
            # Handle range format (e.g., "4-6")
            parts = args.arcs.split("-")
            start, end = int(parts[0]), int(parts[1])
            arc_list = list(range(start, end + 1))
        else:
            # Handle comma-separated format (e.g., "5,6,7")
            arc_list = [int(x.strip()) for x in args.arcs.split(",")]

    beam_path = BETABEAT_DIR / get_measurement_date(squeeze_step) / f"LHCB{beam}/"
    model_base_dir = beam_path / "Models/"
    analysis_base_dir = beam_path / "Results/"

    results_dir = get_results_dir(beam)

    temp_dir = PROJECT_ROOT / "temp_analysis_plots"
    temp_dir.mkdir(exist_ok=True)

    model_dir = model_base_dir / MODEL_DIRS[beam][squeeze_step]
    seq_file = get_or_make_sequence(beam, model_dir)

    fldr_name = "optics" if use_optics else "squeeze"
    estimates_file = PROJECT_ROOT / f"b{beam}_{fldr_name}_results" / f"quad_estimates_{squeeze_step}.txt"
    if not estimates_file.exists():
        print(f"Estimates file not found: {estimates_file}")
        return

    beam_energy, deltap = load_energy_and_deltap_from_metadata(beam, squeeze_step)
    print(f"Beam energy set to: {beam_energy} GeV")

    accelerator = LHC(
        beam=beam,
        beam_energy=beam_energy,
        sequence_file=seq_file,
        optimise_bends=True,
        optimise_quadrupoles=True,
        optimise_other_quadrupoles=True,
        optimise_correctors=False,
        optimise_quad_dx=True,
        optimise_quad_dy=True,
    )

    estimates = load_estimates(estimates_file)
    # Load tune knobs from 0Hz measurement
    tune_knobs_file = results_dir / f"tune_knobs_{squeeze_step}_0Hz.txt"
    if not tune_knobs_file.exists():
        raise FileNotFoundError(f"Tune knobs file not found: {tune_knobs_file}")
    print(f"Using tune knobs file: {tune_knobs_file}")

    corrector_file = results_dir / f"corrector_strengths_{squeeze_step}_{frequency}.txt"
    if not corrector_file.exists():
        raise FileNotFoundError(f"Corrector file not found: {corrector_file}")
    print(f"Using corrector file: {corrector_file}")

    actual = find_true_values(accelerator, estimates, tune_knobs_file)  # , corrector_file)

    plot_quad_diffs(
        estimates,
        actual,
        squeeze_step,
        results_dir,
        fullring=args.fullring,
        accelerator=accelerator if args.fullring else None,
    )

    all_estimates = {}
    for arc in estimates.values():
        all_estimates.update(arc)

    analysis_dir = analysis_base_dir / ANALYSIS_DIRS[beam][squeeze_step]
    if args.fullring:
        plot_fullring_comparison(
            accelerator,
            all_estimates,
            analysis_dir,
            squeeze_step,
            results_dir,
            tune_knobs_file,
            corrector_file,
            beam,
            deltap=deltap,
        )
    else:
        plot_phase_advances(
            accelerator,
            all_estimates,
            analysis_dir,
            squeeze_step,
            results_dir,
            tune_knobs_file,
            corrector_file,
            beam,
            arcs=arc_list,
            deltap=deltap,
        )


if __name__ == "__main__":
    main()
