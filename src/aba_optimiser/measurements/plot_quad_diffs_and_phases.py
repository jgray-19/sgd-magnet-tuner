def load_energy_and_deltap_from_metadata(beam: int, squeeze_step: str) -> tuple[float, float]:
    """Load beam energy and machine deltap from the temp analysis metadata generated by optimisation."""
    metadata_file = (
        PROJECT_ROOT
        / f"temp_analysis_squeeze_b{beam}_{squeeze_step.replace('.', '_')}"
        / "metadata.json"
    )
    if not metadata_file.exists():
        raise FileNotFoundError(
            f"Metadata not found at {metadata_file}. Run optimisation first or regenerate without --skip-reload."
        )
    with metadata_file.open("r") as f:
        metadata = json.load(f)
    energy = float(metadata.get("energy", 450.0))
    deltap = float(metadata.get("machine_deltap", 0.0))
    energy = 450
    deltap = 0.0
    return energy, deltap


def get_twiss_without_errors(
    accelerator: LHC,
    just_bpms: bool,
    estimated_magnets: dict[str, float] | None = None,
    tune_knobs_file: Path | None = None,
    corrector_file: Path | None = None,
    deltap: float = 0.0,
) -> pd.DataFrame:
    """Get twiss data from a model with optional tune knobs and estimated magnets."""
    mad = GradientDescentMadInterface(
        accelerator,
        corrector_strengths=corrector_file,
        tune_knobs_file=tune_knobs_file,
    )
    if estimated_magnets is not None:
        mad.update_knob_values(estimated_magnets)
        # mad.set_magnet_strengths(estimated_magnets)
    return mad.run_twiss(deltap=deltap, observe=int(just_bpms))


def get_fullring_twiss(
    accelerator: LHC,
    estimated_magnets: dict[str, float] | None = None,
    tune_knobs_file: Path | None = None,
    corrector_file: Path | None = None,
    deltap: float = 0.0,
) -> pd.DataFrame:
    """Get full-ring twiss data at BPMs with column names aligned to measurement data."""
    twiss = get_twiss_without_errors(
        accelerator,
        just_bpms=True,
        estimated_magnets=estimated_magnets,
        tune_knobs_file=tune_knobs_file,
        corrector_file=corrector_file,
        deltap=deltap,
    )
    return twiss.rename(columns={"mu1": "mux", "mu2": "muy", "beta11": "betx", "beta22": "bety"})


def find_true_values(
    accelerator: LHC,
    estimates: dict[str, dict[str, float]],
    tune_knobs_file: Path,
    corrector_file: Path | None = None,
) -> dict[str, dict[str, float]]:
    """Find true quadrupole values from the sequence file with tune knobs applied."""
    mad = GradientDescentMadInterface(
        accelerator,
        tune_knobs_file=tune_knobs_file,
        corrector_strengths=corrector_file,
    )
    actual = {}
    for arc, mags in estimates.items():
        actual[arc] = {}
        for mag in mags:
            # elem_name, property_name = mag.rsplit(".", 1)
            # true_k1 = mad.mad.MADX[elem_name][property_name]
            actual[arc][mag] = mad.mad.loaded_sequence[mag]
    return actual


def plot_quad_diffs(
    estimates: dict,
    actual: dict,
    squeeze_step: str,
    results_dir: Path,
    fullring: bool = False,
    accelerator: LHC | None = None,
) -> None:
    """Plot quadrupole differences.

    In full-ring mode, strengths are expected under the ``Arc 1`` header and plotted
    in a single panel.
    """
    if fullring:
        arc_key = "Arc 1"
        if arc_key not in estimates or arc_key not in actual:
            raise KeyError(
                "Full-ring mode expects all strengths under 'Arc 1' in both estimates and actual values."
            )

        mags = list(estimates[arc_key].keys())
        rel_diffs = [
            abs(estimates[arc_key][m] - actual[arc_key][m])
            / abs(actual[arc_key][m])
            * 100
            * np.sign(actual[arc_key][m])
            for m in mags
        ]

        # Separate quads and bends+others
        quads = [m for m in mags if "mq" in m.lower()]
        bends_others = [m for m in mags if "mq" not in m.lower()]

        quad_indices = [mags.index(m) for m in quads]
        bends_others_indices = [mags.index(m) for m in bends_others]

        quad_diffs = [rel_diffs[i] for i in quad_indices]
        bends_others_diffs = [rel_diffs[i] for i in bends_others_indices]

        # Colors for bends_others
        bends_others_colors = []
        for m in bends_others:
            if "mb" in m.lower():
                bends_others_colors.append("blue")
            else:
                bends_others_colors.append("green")

        fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(18, 6))

        # Quadrupoles subplot: if possible, place bars at model s-positions and overlay IP markers.
        if accelerator is not None:
            quad_elems = [m.rsplit(".", 1)[0] for m in quads]
            quad_pos = get_element_positions(accelerator, quad_elems)
            quads_with_pos = [
                (quad_name, quad_pos[quad_name.rsplit(".", 1)[0]])
                for quad_name in quads
                if quad_name.rsplit(".", 1)[0] in quad_pos
            ]
            quads_without_pos = [
                quad_name for quad_name in quads if quad_name.rsplit(".", 1)[0] not in quad_pos
            ]

            if quads_without_pos:
                # If a quadrupole cannot be placed along s, move it to the generic panel.
                bends_others.extend(quads_without_pos)
                bends_others_diffs.extend(
                    [
                        abs(estimates[arc_key][q] - actual[arc_key][q])
                        / abs(actual[arc_key][q])
                        * 100
                        * np.sign(actual[arc_key][q])
                        for q in quads_without_pos
                    ]
                )
                bends_others_colors.extend(["green"] * len(quads_without_pos))
                LOGGER.info(
                    "Moved %d quadrupoles without s-position to 'Bending Magnets & Others' panel.",
                    len(quads_without_pos),
                )

            if quads_with_pos:
                quad_x = [pos for _, pos in quads_with_pos]
                quad_y = [
                    abs(estimates[arc_key][q] - actual[arc_key][q])
                    / abs(actual[arc_key][q])
                    * 100
                    * np.sign(actual[arc_key][q])
                    for q, _ in quads_with_pos
                ]
                ax1.bar(quad_x, quad_y, color="red", width=20.0)
                ax1.set_xlabel("S (m)")
                add_ip_positions_to_plot(ax1, get_ip_positions(accelerator))
            else:
                ax1.text(
                    0.5,
                    0.5,
                    "No quadrupoles with available s-position",
                    transform=ax1.transAxes,
                    ha="center",
                    va="center",
                )
                ax1.set_xticks([])
        else:
            ax1.bar(range(len(quads)), quad_diffs, color="red")
            ax1.set_xticks(range(len(quads)))
            ax1.set_xticklabels([m.split(".")[1] for m in quads], rotation=90)
        ax1.set_title("Quadrupoles")
        ax1.set_ylabel(r"$\Delta k_1 / k_1$ (%)")

        # Bends and others subplot
        ax2.bar(range(len(bends_others)), bends_others_diffs, color=bends_others_colors)
        ax2.set_xticks(range(len(bends_others)))
        ax2.set_xticklabels([m.split(".")[1] for m in bends_others], rotation=90)
        ax2.set_title("Bending Magnets & Others")
        ax2.set_ylabel(r"$\Delta k_0 / k_0$ (%)")

        # Add legend for ax2
        import matplotlib.patches as mpatches

        handles = [
            mpatches.Patch(color="blue", label="Bending magnets"),
            mpatches.Patch(color="green", label="Others"),
        ]
        ax2.legend(handles=handles, loc="upper right")

        plt.tight_layout()
        plt.savefig(results_dir / f"quad_diffs_{squeeze_step}_fullring.png")
        return

    fig, axes = plt.subplots(2, 4, figsize=(20, 10))
    axes = axes.flatten()
    for arc_num in range(1, 9):
        ax = axes[arc_num - 1]
        arc_key = f"Arc {arc_num}"
        if arc_key in estimates:
            mags = list(estimates[arc_key].keys())
            rel_diffs = [
                abs(estimates[arc_key][m] - actual[arc_key][m])
                / abs(actual[arc_key][m])
                * 100
                * np.sign(actual[arc_key][m])
                for m in mags
            ]
            ax.bar(range(len(mags)), rel_diffs)
            ax.set_xticks(range(len(mags)))
            ax.set_xticklabels([m.split(".")[1] for m in mags], rotation=90)
            ax.set_title(f"Arc {arc_num}")
            ax.set_ylabel("Estimated k1")
    plt.tight_layout()
    plt.savefig(results_dir / f"quad_diffs_{squeeze_step}.png")


